                        <div class="unit-body">
                            <h4 class="offset-top-50 section-bottom-30">Mesa de Turismo del Lago Chapo impulsó taller apícola para vecinos</h4>
                            <hr>
                           
                                <p class="offset-top-10 offset-bottom-10 space"> Con el fin de fomentar el emprendimiento en el sector de Río Blanco, 25 vecinos de la zona participaron en el curso “Introducción a la Apicultura” y que se enmarca en el trabajo desarrollado por la Mesa de Turismo y Fomento Productivo del Lago Chapo.<br>
La actividad, que corresponde al primer taller impulsado al amparo de esta mesa, contó con clases teórico-prácticas en las que se abordó la anatomía y desarrollo de las abejas, así como también un acercamiento a los implementos propios de la apicultura, como cajones, alzas, cera, trajes y ahumadores, entre otros, teniendo como eje central la promoción de emprendimientos a través del manejo de la miel y sus derivados.<br>
“El curso me pareció alucinante, porque explica todas las formas para emprender el camino para ser un microempresario. Considero que eso es lo importante de este taller, que las personas puedan emprender con una alternativa de trabajo”, destacó Miguel Ángel Leighton, vecino del sector Río Blanco.<br>
En tanto, el presidente de la junta de vecinos de Río Blanco, Ariel Barría, afirmó que “la apicultura es una labor autosustentable, que servirá para que los vecinos puedan trabajar dentro de su sector de residencia, sin salir de su localidad y obteniendo así el soporte para su hogar”.<br>
Patricia Ríos, otra vecina del sector, valoró la iniciativa, señalando que “este taller beneficiará e incentivará la producción local de miel, ya que muchos apicultores del norte traen sus abejas a nuestros predios a cambio de un kilo de miel por cajón. Las personas aceptan el trato, porque no saben cuánto puede producir un panal”.*<br>
