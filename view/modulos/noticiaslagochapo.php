                            <h4 class="offset-top-50 section-bottom-30">En Lago Chapo inauguraron un paseo en el borde costero</h4>
                            <hr>
                           
                                <p class="offset-top-10 offset-bottom-10 space"> Con un paseo recreativo de una extensión de 700 metros en el borde costero, cuenta desde ahora la localidad de Lago Chapo, en Puerto Montt, con lo cual la comunidad pretende poner en valor su entorno natural.<br>
El proyecto incluye senderos, miradores, señalética informativa, bancas, paneles de interpretación histórica y cultural, balizas que demarcan el área y módulos de venta, entre otros.
La iniciativa impulsada por la Mesa de Turismo y Fomento Productivo del sector fue inaugurada este martes, con presencia de residentes y autoridades.<br>
Para la presidenta de la Junta de Vecinos de la localidad, Susana Gómez, esta es una oportunidad para que surjan emprendimientos relacionados con la artesanía, alojamientos, entre otras actividades.
“Este es un esfuerzo de muchas personas que han tenido el corazón y la valentía para creer en este proyecto, queremos desarrollar este diamante en bruto, dar la reglamentación que corresponde para que esto se disfrute y no se destruya y que nuestros vecinos impulsen sus actividades económicas”, afirmó.<br>
La dirigente insistió en la necesidad de seguir con proyectos como la pavimentación del camino, sistema de agua potable rural y, lo más importante -dijo- que el lago Chapo se convierta en una ruta navegable.
Al respecto, el alcalde (s) de Puerto Montt, Carlos Soto, aseguró que los compromisos del municipio son terminar con la pavimentación del camino que va desde Río Blanco hasta Lago Chapo, alrededor de seis a siete kilómetros. “Con el apoyo del Estado, esperamos construir una ruta navegable que nos permita vincular con la comuna de Cochamó, lo cual va a impulsar el desarrollo del territorio, al calor de las actividades que ellos realizan en su territorio”, sostuvo.<br>
La gobernadora de Llanquihue, Leticia Oyarce, destacó la unión del sector público y privado, enfatizando el apoyo que se ha entregado a la comunidad. “Lago Chapo se verá fuertemente fortalecido con este nuevo paseo de turismo y la invitación es a protegerlo y cuidarlo, lo más importante es continuar en un trabajo permanente que permita llegar a acuerdos para construir el desarrollo de estos sectores rurales y consolidarlos como destinos turísticos”.<br>
El gerente general de Colbun, Thomas Keller, indicó que se busca potenciar el turismo y la actividad económica de este sector, el cual responde a un trabajo arduo basado en las confianzas y en el diálogo.
“Esta es una buena demostración de cómo la empresa privada trabaja con las comunidades que las acoge y las autoridades que representan distintos poderes del Estado. Como Colbun, estamos demostrando que la generación hidroeléctrica se potencia con el desarrollo turístico; tenemos experiencia en Angostura, Colbun Machicura, donde hace un año se inauguró un balneario y, este primer paso apunta a la misma dirección”.<br>
¡Bienvenido!
