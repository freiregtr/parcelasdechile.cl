Otra participante, Patricia Ríos, quien resultó ganadora en la categoría Productos con Identidad Local con su proyecto de artesanía en lana, destacó el apoyo recibido, puesto que "para uno, que está empezando en esto de trabajar la artesanía, fue bien interesante. La verdad es que aprendimos harto y estuvo muy bueno".
Por último, Adelicia Ascencio, vecina del sector de Río Blanco y quien se adjudicó un proyecto de horticultura orgánica en la categoría de Actividades Complementarias, resaltó la oportunidad que cuentan a partir del trabajo de la Mesa de Turismo. "Fue una tremenda oportunidad en el sector y, producto de nuestro esfuerzo, el haber impulsado una actividad. Ha sido un gran impulso para desarrollar el emprendimiento local", sostuvo.*<br></p>
