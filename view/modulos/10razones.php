                             <br>Chile es uno de los países lideres en el consumo de energía solar. La mayoría de nuestros clientes que hoy usan esta opción en sus parcelas, tiene un gasto aproximado de entre $1.700.000 y $2.500.000 en un sistema de paneles solares. 
                             
